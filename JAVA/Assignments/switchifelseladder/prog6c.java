class prog6c{
        public static void main(String[] args){
                int num = 6;
                switch(num){
                        case 1:
                                System.out.println("ONE");
                                break;
                        case 2:
                                System.out.println("TWO");
                                break;
                        case 3:
                                System.out.println("THREE");
                                break;
                        case 4:
                                System.out.println("FOUR");
                                break;
                        case 5:
                                System.out.println("FIVE");
                                break;
                        default:
                                System.out.println("Enter value is greater than 5");
                }
        }
}
