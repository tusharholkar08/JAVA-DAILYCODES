class prog1a{
        public static void main(String[] args){
                int num = 45;
                switch(num%2){
                        case 0:
                                System.out.println("45 is an even no");
                                break;
                        case 1:
                                System.out.println("45 is an odd no");
                                break;
                        default:
                                System.out.println("Invalid no");
                }
        }
}
