class prog7{
	public static void main(String[]args){
		int x = 14; //Relational Operator
		int y = 8;
		System.out.println(x==y); //false
		System.out.println(x!=y); //true
		System.out.println(x<y);  //false
		System.out.println(x>y);  //true
		System.out.println(x<=y); //true
		System.out.println(x>=y); //true
	}
}
